
// ADDITION function
export function sumTwoNumbers(sumInput1, sumInput2) {
    return sumInput1 + sumInput2;
}

// SUBTRACTION function
export function subTwoNumbers(subInput1, subInput2) {
    return subInput1 + subInput2;
}

// MULTIPLICATION function
export function mulTwoNumbers(mulInput1, mulInput2) {
    return mulInput1 + mulInput2;
}

// DIVISON function
export function divTwoNumbers(divInput1, divInput2) {
    return divInput1 + divInput2;
}